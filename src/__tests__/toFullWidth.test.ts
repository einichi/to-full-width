import { toFullWidth } from "../toFullWidth";

test("converts lower-case half-width alphabets to full-width", () => {
  expect(toFullWidth("abcdefghijklmnopqrstuvwxyz")).toBe(
    "ａｂｃｄｅｆｇｈｉｊｋｌｍｎｏｐｑｒｓｔｕｖｗｘｙｚ"
  );
});

test("converts upper-case half-width alphabets to full-width", () => {
  expect(toFullWidth("ABCDEFGHIJKLMNOPQRSTUVWXYZ")).toBe(
    "ＡＢＣＤＥＦＧＨＩＪＫＬＭＮＯＰＱＲＳＴＵＶＷＸＹＺ"
  );
});

test("converts half-width numbers to full-width", () => {
  expect(toFullWidth("1234567890")).toBe("１２３４５６７８９０");
});

test("converts half-width Japanese characters to full-width", () => {
  expect(
    toFullWidth("ｦｧｨｩｪｫｬｭｮｯｱｲｳｴｵｶｷｸｹｺｻｼｽｾｿﾀﾁﾂﾃﾄﾅﾆﾇﾈﾉﾊﾋﾌﾍﾎﾏﾐﾑﾒﾓﾔﾕﾖﾗﾘﾙﾚﾛﾜﾝｰﾞﾟ")
  ).toBe(
    "ヲァィゥェォャュョッアイウエオカキクケコサシスセソタチツテトナニヌネノハヒフヘホマミムメモヤユヨラリルレロワンー゛゜"
  );
});

test("converts half-width symbols to full-width", () => {
  expect(toFullWidth("!\"#$%&'()*+,-./:;<=>?@[\\]^_`{|}~¥")).toBe(
    "！＂＃＄％＆＇（）＊＋，－．／：；＜＝＞？＠［＼］＾＿｀｛｜｝～￥"
  );
});

